
public class NumericalExpressions2 {

	public static void main(String[] args) {
		// 15 + 4
		System.out.println("15 + 4 is " + (15 + 4));		
		// 15 - 4
		System.out.println("15 - 4 is " + (15 - 4));
		// 15 * 4
		System.out.println("15 * 4 is " + (15 * 4));
		// 15 / 4
		System.out.println("15 / 4 is "+ (15 / 4)); // Quotient
		// 15 % 4
		System.out.println("15 % 4 is " + (15 % 4)); // Remainder
				
		// 4 * (15 / 4) + (15 % 4) = 15
		System.out.println("divisor * quotient + remainder = original number: " + (4 * (15 / 4) + (15 % 4)));
				
		System.out.println("5.0 / 2 is " + (5.0 / 2));		
			

	}

}
