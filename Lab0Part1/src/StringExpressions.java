
public class StringExpressions {

	public static void main(String[] args) {
		System.out.print("5 + 2 / 2");
		System.out.print(" is ");
		System.out.println(5 + 4 / 2);
		
		System.out.println("(5 + 2) / 2 is " + ((5 + 4) / 2));		
		System.out.println("(5.0 + 2) / 2 is " + ((5.0 + 2) / 2));
		
		

	}

}
