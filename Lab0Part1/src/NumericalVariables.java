
public class NumericalVariables {

	public static void main(String[] args) {
		int i = 5; // declare i to be a variable string integers only, with an initial value of 5
		System.out.println("i's vaule is " + i);
		i = 7;
		System.out.println("i's value is " + i); // takes value of variable closest above.
		double x = 9.0;
		double y = 7.0;
		System.out.println("Average between x and y is " + ((x + y)/2));
		System.out.println("Average between 7.2 and 3.0 is " + ((x + y)/2));
		System.out.println("Average between " + x + " and " + y + " is " + ((x + y)/2));
	
	}

}
